public class Number_Pat {
    public static void main(String[] args)
    {
        System.out.println("Number Pattern");
        int i,j;
        for(i=1;i<=4;i++)
        {
            for(j=1;j<=4;j++)
            {

                    System.out.print(i);
                    System.out.print(" ");

            }
            System.out.println(" ");
        }

    }
}
